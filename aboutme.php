  <h1>WHO IS NORA?</h1>
  <p>Nora decided to act on her Climate Grief and started looking for solutions that are doing good for the environment and somewhat profitable. She wanted to help lowering meat consumption and at the same time clean the environment. Months of research has started and finally the answer was: A Vienna based company called Hut&Stiel that recycles coffee waste and turns it into delicious Oyster mushroom. Nora started her internship at the company right away.</p>
  <h3>HOW IT ALL STARTED?</h3>
  <p>In the 3rd district of Vienna in Markhofgasse there is a coworking space full of vibrant people with life and energy. Apart from the many people there are many spaces also. At the end of 2019 in one of the basements that is not too big and not too small renewal working has started. Viennese people like to drink coffee. They like to drink coffee a lot and there are thousands of kilograms of coffee grounds ending up in the trash day by day.</p>
  <h3>HOW DOES IT WORK?</h3>
  <p>Instead of going to the garbage the daily collected coffee grounds go to Hut & Stiel's production site where they are carefully turned in to mushroom substrate. The substrate goes in to bags and the bags go in to the incubational room. After 3 weeks of incubating they go to Nora's basement to fruit and to be harvested.</p>
  <h3>TIME TO START!</h3>
  <p>After months of hammering, disassembling, assembling, painting, cleaning, drilling, cleaning, fixing and then cleaning the basement started to appear like a 70 square meters mushroom farm.</p>
  <h3>THE FIRST BAGS HAS ARRIVED!</h3>
  <p>Nora started the production with 50 bags of incubated oyster mushroom (Pleurotus ostreatus) and 4 bags of Lion's Mane (Hericium erinaceus) that are from a Hungarian mushroom company. LaGomba Urban Mushroom</p>
